package com.java.TypeCasting;

public class ImplicitTypeCasting {
	public static void main(String[] args) {
        byte b=10;
        short s=b;
       System.out.println("s:" +s);
      
       float f=75;
       double d=f;
       System.out.println("f:"+f);
       
       char c='a';
       int a=c;
       System.out.println("a:"+a);
       
       short m=45;
       int n=m;
       System.out.println("n:"+n);  
       
}
}
