class AssignmentOperatorAssignment{
public static void main(String[] args){
 
       int t=89;
       int v=10;
       t*=v;
       System.out.println("t:"+t);

       int e=80;
       int f=40;
       e-=f;
       System.out.println("e:"+f);
      
       short o=40;
       short p=40;
       o+=p;
       System.out.println("o:"+o);

       int h=8;
       int g=4;
       h%=g;
       System.out.println("h:"+h);

}
}