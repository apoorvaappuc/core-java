class MixOperators{
public static void main(String[]args){

         int x=10;
         int y=++x;
         int z=y++;
         System.out.println(++z);

         int i=100;
         i++;
         int j=++i;
         int k=j--;
         System.out.println(--k);
 
         int m=5;
         m--;
         --m;
         int n=m;
         System.out.println(++n);

}
}
